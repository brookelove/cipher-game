<script>
import Modal from './Modal.vue';

export default {
  data() {
    return {
      level: this.$route.params.level,
      count: 3,
      answer: '',
      warning: false,
      isPassed: false,
      openModal: false,
      saladText: 'ðŸ¥—',
    };
  },
  methods: {
    checkAnswer(event) {
      event.preventDefault();
      console.log(this.answer.toLowerCase() === 'welcome to the game' )
      if (this.answer.toLowerCase() === 'welcome to the game') {
        this.isPassed = true;
        this.openModal = true;
        let progress = JSON.parse(localStorage.getItem('progress')) || [];
        progress[1] = true;
        localStorage.setItem('progress', JSON.stringify(progress));
        return;
      }
      if (this.count === 1) {
        this.warning = true;
      }
      if (this.count > 0) {
        this.count--;
      }
      if (this.count === 0) {
        this.isPassed = false;
        this.openModal = true;
        this.count = 0;
      }
      this.answer = '';
    },
    handleInputChange(event) {
      this.answer = event.target.value;
    },
    closeModal() {
      this.openModal = false;
    },
    handleMouseOver() {
      this.saladText = '3';
    },
    handleMouseLeave() {
      this.saladText = 'ðŸ¥—';
    }
  },
  components: {
    Modal
  }
};
</script>

